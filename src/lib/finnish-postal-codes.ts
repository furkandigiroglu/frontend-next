export function getCities(): string[] {
  const cities = new Set<string>();
  Object.values(FINNISH_POSTAL_CODES).forEach(data => cities.add(data.city));
  return Array.from(cities).sort();
}

export function getPostalCodesByCity(city: string): string[] {
  return Object.entries(FINNISH_POSTAL_CODES)
    .filter(([_, data]) => data.city.toLowerCase() === city.toLowerCase())
    .map(([code]) => code)
    .sort();
}

export function getPostalCodesByRadius(lat: number, lon: number, radiusKm: number): string[] {
  const codes: string[] = [];
  const R = 6371; // Earth radius in km

  Object.entries(FINNISH_POSTAL_CODES).forEach(([code, data]) => {
    const dLat = (data.lat - lat) * Math.PI / 180;
    const dLon = (data.lon - lon) * Math.PI / 180;
    const a = 
      Math.sin(dLat/2) * Math.sin(dLat/2) +
      Math.cos(lat * Math.PI / 180) * Math.cos(data.lat * Math.PI / 180) * 
      Math.sin(dLon/2) * Math.sin(dLon/2);
    const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
    const distance = R * c;

    if (distance <= radiusKm) {
      codes.push(code);
    }
  });

  return codes.sort();
}

